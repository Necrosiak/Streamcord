type Locale = "en" | "fr" | "de" | "es" | "it" | "pt" | "nl" | "pl" | "ru";
type Dict = Record<string, string>;

const STEAM_MAP: Record<string, Locale> = {
  english: "en", french: "fr", german: "de",
  spanish: "es", portuguese: "pt", italian: "it",
  dutch: "nl", polish: "pl", russian: "ru",
};
const NAV_MAP: Record<string, Locale> = {
  en: "en", fr: "fr", de: "de", es: "es",
  it: "it", pt: "pt", nl: "nl", pl: "pl", ru: "ru",
};

function detectLocale(): Locale {
  try {
    const lm = (window as any).LocalizationManager;
    for (const k of ["m_strLanguage", "strLanguage", "language"]) {
      const v: string = lm?.[k];
      if (typeof v === "string" && STEAM_MAP[v.toLowerCase()]) return STEAM_MAP[v.toLowerCase()];
    }
  } catch {}
  try {
    const nav = navigator.language?.split("-")[0].toLowerCase();
    if (nav && NAV_MAP[nav]) return NAV_MAP[nav];
  } catch {}
  return "en";
}

